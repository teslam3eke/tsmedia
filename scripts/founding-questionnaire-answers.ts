/** 固定第 6 題：founding_member_no 1–50 各對一則，保證 50 人互不重複 */
export function turningPointAnswerForFoundingNo(no: number): string {
  const pool = FOUNDING_ANSWERS_BY_QUESTION_ID[9000]
  if (!pool?.length) {
    return '談未來我會先分清楚想要跟應該 也留空間調整 不把承諾說死'
  }
  const idx = Math.max(0, Math.min(pool.length - 1, no - 1))
  return pool[idx]!
}
